package com.msa.demo.sockshop.model;

import java.util.List;

public class Catalogue {
    private String id;
    private String name;
    private String description;
    private List<Image> images;
    private float price;
    private int stock;
    private List<Tag> tags;

    public Catalogue(String id, String name, String description, float price, int stock) {
        this.id = id;
        this.name = name;
        this.description = description;
        this.price = price;
        this.stock = stock;
    }

    public Catalogue(String name, String description, List<Image> images, float price, int stock, List<Tag> tags) {
        this.name = name;
        this.description = description;
        this.images = images;
        this.price = price;
        this.stock = stock;
        this.tags = tags;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public List<Image> getImages() {
        return images;
    }

    public void setImages(List<Image> images) {
        this.images = images;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }

    public List<Tag> getTags() {
        return tags;
    }

    public void setTags(List<Tag> tags) {
        this.tags = tags;
    }
}
